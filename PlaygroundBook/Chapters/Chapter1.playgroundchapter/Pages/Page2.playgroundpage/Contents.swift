//:# More Green, More life
//: A simple attitude of planting a tree can bring many benefits, not only for those who planted, but also to everyone around.
//: Some benefits that planting a tree can bring:
//:
//: * **Reduction of air pollution** \
//: Trees improve the quality of the air we breathe. During their growth, trees attach CO2 to the roots, trunks, branches and leaves. The trees filter out polluting gases like ozone, carbon monoxide and sulfur dioxide from the air we breathe.
//:
//: * **Reduction of ambient temperature** \
//: Trees absorb the solar radiation, contributing to the reduction of the ambient temperature.
//:
//: * **Emit Oxygen** \
//: Trees emit oxygen, the gas we breathe.
